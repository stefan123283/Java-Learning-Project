package com.stefanfrunzaqa.operationsandciclicfunctions;

public class ComparisonOperationsExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        boolean result = a == b;
        System.out.println("10 = 5: " + result);
        result = a != b;
        System.out.println("10 != 5: " + result);
        result = a > b;
        System.out.println("10 > 5: " + result);
        result = a >= b;
        System.out.println("10 >= 5: " + result);
        result = a < b;
        System.out.println("10 < 5: " + result);
        result = a <= b;
        System.out.println("10 <= 5: " + result);
    }
}